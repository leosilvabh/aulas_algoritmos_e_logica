# Crie um programa que liste  
# os 10 primeiros pares

contador = 1
while contador <= 10: #while é enquanto
    print (contador * 2)# toda função tem que estar entre parentese print é para escrever na tela. Multiplica por dois pois ele esta pedindo os pares
    contador += 1 # essa sintaxe é a mesma coisa que contador = contador + 1
    print ("estou dentro do while")
print ("estou fora do while")